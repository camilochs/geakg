# =============================================================================
# DomainContext for agnostic operators (Lampson: "Keep secrets")
# =============================================================================
class TSPContext:
    """TSP domain context - hides distance_matrix from operators."""

    def __init__(self, distance_matrix: list[list[float]]):
        self._dm = distance_matrix
        self._n = len(distance_matrix)

    def cost(self, tour: list[int], i: int) -> float:
        """Cost contribution of city at position i."""
        n = len(tour)
        prev_i, next_i = (i - 1) % n, (i + 1) % n
        return self._dm[tour[prev_i]][tour[i]] + self._dm[tour[i]][tour[next_i]]

    def delta(self, tour: list[int], move: str, i: int, j: int) -> float:
        """Delta cost if move(i,j) were applied."""
        if move == "swap":
            return self._swap_delta(tour, i, j)
        elif move == "2opt":
            return self._2opt_delta(tour, i, j)
        return 0.0

    def _swap_delta(self, tour: list[int], i: int, j: int) -> float:
        old_cost = self.cost(tour, i) + self.cost(tour, j)
        tour[i], tour[j] = tour[j], tour[i]
        new_cost = self.cost(tour, i) + self.cost(tour, j)
        tour[i], tour[j] = tour[j], tour[i]
        return new_cost - old_cost

    def _2opt_delta(self, tour: list[int], i: int, j: int) -> float:
        if i > j:
            i, j = j, i
        n = len(tour)
        a, b = tour[i], tour[(i + 1) % n]
        c, d = tour[j], tour[(j + 1) % n]
        return (self._dm[a][c] + self._dm[b][d]) - (self._dm[a][b] + self._dm[c][d])

    def neighbors(self, tour: list[int], i: int, k: int) -> list[int]:
        """K nearest positions by distance."""
        city = tour[i]
        dists = [(j, self._dm[city][tour[j]]) for j in range(len(tour)) if j != i]
        dists.sort(key=lambda x: x[1])
        return [idx for idx, _ in dists[:k]]

    def evaluate(self, tour: list[int]) -> float:
        """Total tour cost."""
        return sum(self._dm[tour[i]][tour[(i + 1) % len(tour)]] for i in range(len(tour)))

    def valid(self, tour: list[int]) -> bool:
        """Check if tour is valid permutation."""
        return len(tour) == self._n and set(tour) == set(range(self._n))


