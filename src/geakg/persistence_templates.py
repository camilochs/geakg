def tour_cost(tour: list[int], distance_matrix: list[list[float]]) -> float:
    """Calculate total tour cost."""
    cost = 0.0
    n = len(tour)
    for i in range(n):
        cost += distance_matrix[tour[i]][tour[(i + 1) % n]]
    return cost


def load_tsplib(filepath: str) -> tuple[list[list[float]], float | None]:
    """Load TSPLIB format file.

    Returns:
        Tuple of (distance_matrix, optimal_cost or None)
    """
    with open(filepath) as f:
        content = f.read()

    lines = content.strip().split("\\n")
    dimension = 0
    edge_weight_type = "EUC_2D"
    optimal_cost = None
    i = 0

    while i < len(lines):
        line = lines[i].strip()
        if line.startswith("DIMENSION"):
            dimension = int(line.split(":")[1].strip())
        elif line.startswith("EDGE_WEIGHT_TYPE"):
            edge_weight_type = line.split(":")[1].strip()
        elif line.startswith("NODE_COORD_SECTION"):
            i += 1
            break
        elif "optimal" in line.lower():
            import re
            match = re.search(r"(\\d+(?:\\.\\d+)?)", line)
            if match:
                optimal_cost = float(match.group(1))
        i += 1

    coordinates = []
    while i < len(lines):
        line = lines[i].strip()
        if line == "EOF" or line.startswith("DISPLAY"):
            break
        parts = line.split()
        if len(parts) >= 3:
            x, y = float(parts[1]), float(parts[2])
            coordinates.append((x, y))
        i += 1

    n = len(coordinates)
    matrix = [[0.0] * n for _ in range(n)]

    for i in range(n):
        for j in range(n):
            if i != j:
                x1, y1 = coordinates[i]
                x2, y2 = coordinates[j]
                if edge_weight_type == "EUC_2D":
                    matrix[i][j] = math.sqrt((x2 - x1) ** 2 + (y2 - y1) ** 2)
                elif edge_weight_type == "CEIL_2D":
                    matrix[i][j] = math.ceil(math.sqrt((x2 - x1) ** 2 + (y2 - y1) ** 2))
                else:
                    matrix[i][j] = math.sqrt((x2 - x1) ** 2 + (y2 - y1) ** 2)

    return matrix, optimal_cost


def run_program(
    distance_matrix: list[list[float]],
    n_runs: int = 10,
    timeout: float | None = None,
    optimal: float | None = None,
    verbose: bool = False,
) -> tuple[list[int], float, int]:
    """Execute the best program on a distance matrix.

    All operators use the domain-agnostic signature: operator(solution, ctx)
    Following Lampson's "Keep secrets" - distance_matrix is hidden in ctx.

    Args:
        distance_matrix: Square distance matrix.
        n_runs: Number of independent runs.
        timeout: Maximum time in seconds (None = no limit).
        optimal: Known optimal for gap calculation.
        verbose: Print progress.

    Returns:
        Tuple of (best_tour, best_cost, runs_completed)
    """
    import time
    best_tour, best_cost = None, float("inf")
    n = len(distance_matrix)
    start_time = time.time()
    runs_completed = 0

    # Create context once - operators use ctx, not distance_matrix directly
    ctx = TSPContext(distance_matrix)

    for run in range(n_runs):
        # Check timeout
        if timeout and (time.time() - start_time) >= timeout:
            if verbose:
                print(f"\\n[Timeout reached after {{runs_completed}} runs]")
            break

        # Start with random permutation
        solution = list(range(n))
        random.shuffle(solution)

        # Apply operator sequence using agnostic signature
        for op_name in BEST_OPERATOR_PATH:
            if op_name not in OPERATORS:
                if verbose:
                    print(f"Warning: operator {{op_name}} not found, skipping")
                continue

            op = OPERATORS[op_name]

            # All operators use agnostic signature: (solution, ctx)
            try:
                result = op(solution, ctx)
                if isinstance(result, list) and len(result) == n and ctx.valid(result):
                    solution = result
            except Exception as e:
                if verbose:
                    print(f"Warning: operator {{op_name}} failed: {{e}}")

        cost = ctx.evaluate(solution)
        improved = cost < best_cost
        if improved:
            best_tour, best_cost = solution.copy(), cost

        runs_completed += 1

        if verbose and improved:
            gap_str = ""
            if optimal and optimal > 0:
                gap = 100 * (cost - optimal) / optimal
                gap_str = f" (gap={{gap:.2f}}%)"
            print(f"Run {{run + 1}}/{{n_runs}}: cost={{cost:.2f}}{{gap_str}} *")

    return best_tour, best_cost, runs_completed


def main(args: list[str]) -> None:
    """Main entry point."""
    import argparse
    import json
    parser = argparse.ArgumentParser(description="Run NS-SE best program on TSP instance")
    parser.add_argument("instance", help="Path to TSPLIB instance file")
    parser.add_argument("-n", "--n-runs", type=int, default=None, help="Number of runs (default: 10, or unlimited if timeout set)")
    parser.add_argument("-t", "--timeout", type=float, default=None, help="Timeout in seconds (default: no limit)")
    parser.add_argument("--optimal", type=float, default=None, help="Known optimal cost (overrides file)")
    parser.add_argument("-q", "--quiet", action="store_true", help="Suppress per-run output")
    parser.add_argument("-o", "--output", type=str, default=None, help="Save results to JSON file")
    parsed = parser.parse_args(args)

    # If timeout is set and n_runs not specified, use unlimited runs
    if parsed.timeout and parsed.n_runs is None:
        n_runs = 999999999  # Effectively unlimited
    else:
        n_runs = parsed.n_runs or 10  # Default to 10 if neither specified

    print(f"Loading instance: {{parsed.instance}}")
    distance_matrix, file_optimal = load_tsplib(parsed.instance)
    optimal = parsed.optimal or file_optimal
    print(f"Instance size: {{len(distance_matrix)}} cities")

    if optimal:
        print(f"Known optimal: {{optimal:.2f}}")

    print(f"\\nRunning best program...")
    if parsed.timeout:
        print(f"  Timeout: {{parsed.timeout}}s")
        if parsed.n_runs is None:
            print(f"  Runs: unlimited (until timeout)")
        else:
            print(f"  Runs: {{n_runs}} (max)")
    else:
        print(f"  Runs: {{n_runs}}")
    print(f"  Operators: {{len(BEST_OPERATOR_PATH)}} in path")
    if BEST_OPERATOR_PATH:
        print(f"  Path: {{BEST_OPERATOR_PATH[:3]}}..." if len(BEST_OPERATOR_PATH) > 3 else f"  Path: {{BEST_OPERATOR_PATH}}")
    print()

    best_tour, best_cost, runs_completed = run_program(
        distance_matrix,
        n_runs=n_runs,
        timeout=parsed.timeout,
        optimal=optimal,
        verbose=not parsed.quiet,
    )

    print(f"\\n{{'=' * 50}}")
    print(f"Runs completed: {{runs_completed}}")
    print(f"Best tour cost: {{best_cost:.2f}}")
    if optimal and optimal > 0:
        gap = 100 * (best_cost - optimal) / optimal
        print(f"Gap to optimal: {{gap:.2f}}%")
    if best_tour:
        print(f"Tour: {{best_tour[:10]}}..." if len(best_tour) > 10 else f"Tour: {{best_tour}}")


if __name__ == "__main__":
    main(sys.argv[1:])
