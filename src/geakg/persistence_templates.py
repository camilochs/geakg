def _invert(perm: list[int], i: int | None = None, j: int | None = None) -> list[int]:
    """Invert (reverse) segment from position i to j."""
    n = len(perm)
    if n < 2:
        return perm[:]
    if i is None or j is None:
        i, j = sorted(random.sample(range(n), 2))
    if i > j:
        i, j = j, i
    result = perm[:]
    result[i : j + 1] = reversed(result[i : j + 1])
    return result


def _insert(perm: list[int], i: int | None = None, j: int | None = None) -> list[int]:
    """Remove element at position i and insert at position j."""
    n = len(perm)
    if n < 2:
        return perm[:]
    if i is None:
        i = random.randint(0, n - 1)
    if j is None:
        j = random.randint(0, n - 1)
        while j == i:
            j = random.randint(0, n - 1)
    result = perm[:]
    elem = result.pop(i)
    result.insert(j, elem)
    return result


def insert(perm: list[int], i: int | None = None, j: int | None = None) -> list[int]:
    """Public insert wrapper."""
    return _insert(perm, i, j)


def invert(perm: list[int], i: int | None = None, j: int | None = None) -> list[int]:
    """Public invert wrapper."""
    return _invert(perm, i, j)


