PROGRAM_TEMPLATES = {
    "tsp": TSP_PROGRAM_TEMPLATE,
    "jssp": JSSP_PROGRAM_TEMPLATE,
    "vrp": VRP_PROGRAM_TEMPLATE,
    "bpp": BPP_PROGRAM_TEMPLATE,
}


def get_program_template(domain: str) -> str:
    """Get program template for a domain.

    Args:
        domain: Domain name (tsp, jssp, vrp, bpp).

    Returns:
        Template string.

    Raises:
        ValueError: If domain not supported.
    """
    domain = domain.lower()
    if domain not in PROGRAM_TEMPLATES:
        raise ValueError(
            f"Unknown domain '{domain}'. Supported: {list(PROGRAM_TEMPLATES.keys())}"
        )
    return PROGRAM_TEMPLATES[domain]
