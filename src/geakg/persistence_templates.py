def _wrap_vnd_generic(solution: list[int], ctx) -> list[int]:
    """Variable neighborhood descent using ctx.evaluate."""
    best = solution[:]
    best_cost = ctx.evaluate(best)

    neighborhoods = [swap, insert, invert]
    improved = True
    max_iters = 20
    iters = 0

    while improved and iters < max_iters:
        improved = False
        iters += 1
        for neighborhood in neighborhoods:
            candidate = neighborhood(best)
            candidate_cost = ctx.evaluate(candidate)
            if candidate_cost < best_cost:
                best = candidate
                best_cost = candidate_cost
                improved = True
                break
    return best


